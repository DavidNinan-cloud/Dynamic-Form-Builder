import { toast } from "react-toastify";


export const successAlert = (message) => {
  // window.alert("Invalid Credentials");
  toast.success(message, {
    position: "top-right",
    autoClose: 4000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: false,
    draggable: true,
    progress: undefined,
  });
};
